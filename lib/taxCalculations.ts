/**
 * Slå upp skattebeloppet för given bruttolön via linjär interpolation.
 * Returnerar månatligt skatteavdrag i kronor.
 */
export function lookupTax(grossMonthly: number, tableNum = 32): number {
  if (grossMonthly <= 0) return 0;

  const table = TAX_TABLES[tableNum] ?? TAX_TABLES[32];

  // Under tabellens nedre gräns
  if (grossMonthly <= table[0][0]) return table[0][1];

  // Över tabellens övre gräns – extrapolera med sista segmentets lutning
  if (grossMonthly >= table[table.length - 1][0]) {
    const [x0, y0] = table[table.length - 2];
    const [x1, y1] = table[table.length - 1];
    const rate = (y1 - y0) / (x1 - x0);
    return Math.round(y1 + rate * (grossMonthly - x1));
  }

  // Hitta kringliggande intervall och interpolera linjärt
  for (let i = 1; i < table.length; i++) {
    const [x0, y0] = table[i - 1];
    const [x1, y1] = table[i];
    if (grossMonthly <= x1) {
      const t = (grossMonthly - x0) / (x1 - x0);
      return Math.round(y0 + t * (y1 - y0));
    }
  }

  return 0;
}

/** Arbetsgivaravgift – 31,42 % (2026) */
export const EMPLOYERS_FEE_RATE = 0.3142;

/** Bolagsskatt – 20,0 % */
export const CORPORATE_TAX_RATE = 0.20;

/** Kapitalskatt på utdelning enligt 3:12-reglerna – 20,0 % */
export const DIVIDEND_TAX_RATE = 0.20;

/** Särskild löneskatt på pensionskostnader – 24,26 % (2026) */
export const SPECIAL_WAGE_TAX_RATE = 0.2426;

export interface CalculatorInputs {
  invoicedAmount: number;      // Fakturerat belopp kr/mån
  grossSalary: number;         // Bruttolön kr/mån
  otherCosts: number;          // Övriga fasta bolagskostnader kr/mån
  pensionContribution: number; // Pensionsavsättning kr/mån
  taxTableNum?: number;        // Skattetabell (default 32)
}

export interface CalculatorResults {
  // Lönekostnader
  employersFee: number;        // Arbetsgivaravgift
  totalSalaryCost: number;     // Bruttolön + arbetsgivaravgift

  // Skatt på lön
  incomeTax: number;           // Skatt enligt vald tabell
  netSalary: number;           // Nettolön (utbetald)

  // Bolaget
  specialWageTax: number;      // Särskild löneskatt på pension (24,26 %)
  totalPensionCost: number;    // Pensionsavsättning + SLS
  companyRemainder: number;    // Kvar i bolaget före bolagsskatt
  corporateTax: number;        // Bolagsskatt 20,0 %
  possibleDividend: number;    // Möjlig utdelning (före kapitalskatt)
  dividendAfterTax: number;    // Utdelning efter 20% kapitalskatt (3:12)

  // Summering för stapeldiagram
  breakdown: {
    netSalary: number;
    incomeTax: number;
    employersFee: number;
    otherCosts: number;
    pension: number;
    corporateTax: number;
    dividend: number;
  };
}

/**
 * Kör samtliga beräkningar givet indata.
 */
export function calculate(inputs: CalculatorInputs): CalculatorResults {
  const { invoicedAmount, grossSalary, otherCosts, pensionContribution, taxTableNum = 32 } = inputs;

  // 1. Arbetsgivaravgift
  const employersFee = Math.round(grossSalary * EMPLOYERS_FEE_RATE);

  // 2. Total lönekostnad för bolaget
  const totalSalaryCost = grossSalary + employersFee;

  // 3. Särskild löneskatt på pensionskostnader (24,26 %)
  const specialWageTax = Math.round(pensionContribution * SPECIAL_WAGE_TAX_RATE);
  const totalPensionCost = pensionContribution + specialWageTax;

  // 4. Kvar i bolaget (kan vara negativt om lönen är hög)
  const companyRemainder = invoicedAmount - totalSalaryCost - otherCosts - totalPensionCost;

  // 5. Bolagsskatt – enbart om kvar i bolaget > 0
  const corporateTax = companyRemainder > 0
    ? Math.round(companyRemainder * CORPORATE_TAX_RATE)
    : 0;

  // 6. Möjlig utdelning (före kapitalskatt)
  const possibleDividend = companyRemainder > 0
    ? companyRemainder - corporateTax
    : companyRemainder; // negativt → inga pengar kvar

  // 7. Kapitalskatt på utdelning 20 % (3:12-reglerna)
  const dividendAfterTax = possibleDividend > 0
    ? Math.round(possibleDividend * (1 - DIVIDEND_TAX_RATE))
    : possibleDividend;

  // 8. Skatt på lön via vald skattetabell
  const incomeTax = lookupTax(grossSalary, taxTableNum);

  // 9. Nettolön
  const netSalary = grossSalary - incomeTax;

  return {
    employersFee,
    totalSalaryCost,
    specialWageTax,
    totalPensionCost,
    incomeTax,
    netSalary,
    companyRemainder,
    corporateTax,
    possibleDividend,
    dividendAfterTax,
    breakdown: {
      netSalary: Math.max(0, netSalary),
      incomeTax,
      employersFee,
      otherCosts,
      pension: pensionContribution + specialWageTax,
      corporateTax: Math.max(0, corporateTax),
      dividend: Math.max(0, possibleDividend),
    },
  };
}

/** Formatera kronor med tusentalsavgränsare */
export function formatKr(amount: number): string {
  return new Intl.NumberFormat("sv-SE", {
    style: "currency",
    currency: "SEK",
    maximumFractionDigits: 0,
  }).format(amount);
}
